// ---------------------------------------------------------------------------
// Feed triangles from one fixed-stride source row into a builder.
// ---------------------------------------------------------------------------
void FeedTriangles(const int* row, int srcStride, vtkPolygonBuilder& builder)
{
  for (int i = 0; i + 2 < srcStride; i += 3)
  {
    if (row[i] == -1)
      break;
    vtkIdType tri[3] = { row[i], row[i + 1], row[i + 2] };
    builder.InsertTriangle(tri);
  }
}

// ---------------------------------------------------------------------------
// Encode polygons from a collection into a fixed-stride output vector.
// Format (non-empty): nPts0 e0..eN  nPts1 e0..eN  ... -1  -1(pad)
// Format (empty):     -1  -1(pad)
// ---------------------------------------------------------------------------
std::vector<int> EncodePolygons(vtkIdListCollection* polys, int dstStride)
{
  std::vector<int> out;

  vtkCollectionSimpleIterator cookie;
  polys->InitTraversal(cookie);
  while (vtkIdList* poly = polys->GetNextIdList(cookie))
  {
    out.push_back(static_cast<int>(poly->GetNumberOfIds()));
    for (vtkIdType i = 0; i < poly->GetNumberOfIds(); ++i)
    {
      out.push_back(static_cast<int>(poly->GetId(i)));
    }
    poly->Delete(); // GetPolygons adds a reference, so clean up as we go
  }

  if (out.empty())
  {
    out.assign(dstStride, -1);
  }
  else
  {
    out.push_back(-1); // terminator
    while (static_cast<int>(out.size()) < dstStride)
    {
      out.push_back(-1); // pad to fixed stride
    }
  }

  return out;
}

// ---------------------------------------------------------------------------
// Print a full polygon case table derived from a triangle table.
// Validates that dstStride is sufficient for all cases before emitting.
// ---------------------------------------------------------------------------
void PrintPolyTable(const char* typeName, const char* tableName, const int* srcData, int numCases,
  int srcStride, int dstStride)
{
  // Single collection reused across all cases (GetPolygons calls RemoveAllItems)
  vtkIdListCollection* polys = vtkIdListCollection::New();

  // Validate dstStride before emitting anything
  for (int c = 0; c < numCases; ++c)
  {
    vtkPolygonBuilder builder;
    FeedTriangles(srcData + c * srcStride, srcStride, builder);
    builder.GetPolygons(polys);

    int needed = 1; // terminator
    vtkCollectionSimpleIterator cookie;
    polys->InitTraversal(cookie);
    while (vtkIdList* poly = polys->GetNextIdList(cookie))
    {
      needed += 1 + static_cast<int>(poly->GetNumberOfIds());
      poly->Delete(); // GetPolygons adds a reference, so clean up as we go
    }

    if (needed > dstStride)
    {
      fprintf(stderr, "ERROR: %s case %d needs %d ints but dstStride is %d\n", tableName, c, needed,
        dstStride);
    }
  }

  // Emit table
  printf("constexpr %s %s[%d][%d] = {\n", typeName, tableName, numCases, dstStride);
  for (int c = 0; c < numCases; ++c)
  {
    vtkPolygonBuilder builder;
    FeedTriangles(srcData + c * srcStride, srcStride, builder);
    builder.GetPolygons(polys);

    auto out = EncodePolygons(polys, dstStride);
    printf("  {");
    for (int i = 0; i < static_cast<int>(out.size()); ++i)
    {
      printf(" %d%s", out[i], i + 1 < static_cast<int>(out.size()) ? "," : "");
    }
    printf(" }, /* %d */\n", c);
  }
  printf("};\n\n");

  polys->Delete();
}

